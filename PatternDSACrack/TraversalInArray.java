package Array;
// for loop
public class TraversalInArray {
    public class ArrayTraversal {
        public static void main(String[] args) {
            int[] arr = {10, 20, 30, 40, 50};
    
            // Traversing using for loop
            for (int i = 0; i < arr.length; i++) {
                System.out.print(arr[i] + " ");
            }
        }
    }
    
}

